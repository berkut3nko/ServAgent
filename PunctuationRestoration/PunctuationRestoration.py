import re

text = "коли буде наступний вихідний як я можу оформити депозит що таке вільний час"

# Розбиваємо текст на потенційні запитання
sentences = re.split(r"(?=\b(чим|чи|чому|яким|яку|які|як|скільки|чого|що|куди|коли|хто|де|чому)\b)", text)

# Робимо першу букву великою та додаємо "?"
questions = [sent.capitalize() + "?" for sent in sentences]

print(questions)